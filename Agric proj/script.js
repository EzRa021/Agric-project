function myfunction1() {
  const num1 = document.querySelector("#num1");
  const num2 = document.querySelector("#num2");
  const num3 = document.querySelector("#num3");
  const num4 = document.querySelector("#num4");

  const context1 = document.querySelector("#context1");
  const context2 = document.querySelector("#context2");
  const context3 = document.querySelector("#context3");
  const context4 = document.querySelector("#context4");

  context1.style.display = "flex"
  context1.style.display = "block"
  context2.style.display = "none"
  context3.style.display = "none"
  context4.style.display = "none"

  num4.style.background = "none"
  num1.style.background = "white"
  num1.style.color = "black"
  num2.style.background = "none"
  num3.style.background = "none"

  num4.style.color = "white"
  num3.style.color = "white"
  num2.style.color = "white"
  num1.style.color = "lawngreen"





}


